package org.raystack.dagger.core.exception;

/**
 * The class Exception if there is an Invalid Longbow duration.
 */
public class InvalidLongbowDurationException extends RuntimeException {
    /**
     * Instantiates a new Invalid longbow duration exception.
     *
     * @param message the message
     */
    public InvalidLongbowDurationException(String message) {
        super(message);
    }
}
